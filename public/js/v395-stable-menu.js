/* V395 — menu stable, sans doublons, avec navigation fonctionnelle. */
(()=>{
  'use strict';
  let rebuilding=false;
  let scheduled=0;

  const isEnglish=()=>{ try { return typeof lang!=='undefined' && lang==='en'; } catch(_) { return false; } };
  const labels=()=>isEnglish()?{
    home:'Home',characters:'Characters',tunings:'T.U.N.I.N.G',costumes:'Costumes',builds:'Community Builds',mods:'Mods',creator:'Creator Ranking',tier:'Tier List',search:'Global Search',install:'Preparing installation…',game:'GAME',community:'COMMUNITY',tools:'TOOLS'
  }:{
    home:'Accueil',characters:'Personnages',tunings:'T.U.N.I.N.G',costumes:'Costumes',builds:'Builds communauté',mods:'Mods',creator:'Classement créateurs',tier:'Tier List',search:'Recherche globale',install:'Préparation de l’installation…',game:'JEU',community:'COMMUNAUTÉ',tools:'OUTILS'
  };
  const icons={home:'🏠',characters:'🦸',tunings:'🧬',costumes:'👕',builds:'👥',mods:'🧩',creator:'🏅',tier:'🏆',search:'🔎',install:'📲'};

  function section(key,title){
    const s=document.createElement('section');
    s.className='v386-menu-section';
    s.dataset.v395Section=key;
    const h=document.createElement('div');
    h.className='v386-menu-section-title';
    h.textContent=title;
    s.appendChild(h);
    return s;
  }

  function makeCustom(key){
    const b=document.createElement('button');
    b.type='button';
    b.className='navItem';
    b.dataset.mhurHub='1';
    b.dataset.v395Key=key;
    if(key==='mods') b.dataset.mhurMods='1';
    if(key==='install') b.dataset.mhurInstall='1';
    else b.dataset.mhurFinalNav=key;
    return b;
  }

  function runCustom(key){
    document.getElementById('drawer')?.classList.remove('open');
    if(key==='mods') return window.MHUR_MODS?.open?.();
    if(key==='creator') return window.MHUR_V370?.openLeaderboard?.();
    if(key==='tier') return window.MHUR_HUB?.tier?.open?.();
    if(key==='search') return window.MHUR_HUB?.search?.open?.();
    if(key==='install') return window.MHUR_V370?.installApp?.();
  }

  function build(){
    if(rebuilding) return;
    const drawer=document.getElementById('drawer');
    if(!drawer) return;
    rebuilding=true;
    try{
      // Keep only the five real page links generated by the main app.
      const base={};
      for(const key of ['home','characters','tunings','costumes','builds']){
        const all=[...drawer.querySelectorAll(`.navItem[data-page="${key}"]`)];
        if(all[0]) base[key]=all[0];
        all.slice(1).forEach(x=>x.remove());
      }

      // Remove every previously generated community/tool item and section.
      [...drawer.querySelectorAll('.v386-menu-section,[data-v395-section]')].forEach(x=>x.remove());
      [...drawer.querySelectorAll('.navItem:not([data-page])')].forEach(x=>x.remove());

      const custom={};
      for(const key of ['mods','creator','tier','search','install']) custom[key]=makeCustom(key);
      custom.mods.onclick=e=>{e.preventDefault();e.stopPropagation();runCustom('mods');};
      custom.creator.onclick=e=>{e.preventDefault();e.stopPropagation();runCustom('creator');};
      custom.tier.onclick=e=>{e.preventDefault();e.stopPropagation();runCustom('tier');};
      custom.search.onclick=e=>{e.preventDefault();e.stopPropagation();runCustom('search');};
      custom.install.onclick=e=>{e.preventDefault();e.stopPropagation();runCustom('install');};

      const l=labels();
      for(const [key,node] of Object.entries({...base,...custom})){
        if(!node) continue;
        node.innerHTML=`<span class="v395-nav-icon" aria-hidden="true">${icons[key]}</span><span class="v395-nav-label">${l[key]}</span>`;
        node.classList.toggle('v386-menu-home',key==='home');
      }

      drawer.replaceChildren();
      if(base.home) drawer.appendChild(base.home);
      const game=section('game',l.game);
      ['characters','tunings','costumes'].forEach(k=>base[k]&&game.appendChild(base[k]));
      const community=section('community',l.community);
      ['builds'].forEach(k=>base[k]&&community.appendChild(base[k]));
      ['mods','creator','tier'].forEach(k=>community.appendChild(custom[k]));
      const tools=section('tools',l.tools);
      ['search','install'].forEach(k=>tools.appendChild(custom[k]));
      drawer.append(game,community,tools);
    } finally {
      rebuilding=false;
    }
  }

  function schedule(delay=0){
    clearTimeout(scheduled);
    scheduled=setTimeout(build,delay);
  }

  // Rebuild after the app recreates the drawer.
  const previous=window.layout;
  if(typeof previous==='function'&&!previous.__v395){
    const wrapped=function(){
      const result=previous.apply(this,arguments);
      schedule(0);
      return result;
    };
    wrapped.__v395=true;
    window.layout=wrapped;
    try { layout=wrapped; } catch(_) {}
  }

  document.addEventListener('DOMContentLoaded',()=>schedule(0),{once:true});
  window.addEventListener('load',()=>schedule(50),{once:true});
  document.addEventListener('click',e=>{
    if(e.target.closest('#langBtn,.langBtn,[data-lang]')) schedule(30);
  });
  window.addEventListener('hashchange',()=>schedule(30));
  setTimeout(()=>schedule(0),100);
  setTimeout(()=>schedule(0),500);
  window.MHUR_V395={refreshMenu:build};
})();
